e, f = 100, 200, 300
e #=> 100
f #=> 200

# ----------------------------------------

e, *f = 100, 200, 300
e #=> 100
f #=> [200, 300]

# ----------------------------------------

# 100だけeに格納して、残りの要素は無視する
e, * = 100, 200, 300
e #=> 100

# ----------------------------------------

# *を省略して200と300を無視する
e, = 100, 200, 300
e #=> 100

# ----------------------------------------

a, *b, c, d = 1, 2, 3, 4, 5
a #=> 1
b #=> [2, 3]
c #=> 4
d #=> 5

# ----------------------------------------

# 1がa、2がc、3がdに対応する。右辺に残りの要素はなくなったのでbは空の配列になる
a, *b, c, d = 1, 2, 3
a #=> 1
b #=> []
c #=> 2
d #=> 3
